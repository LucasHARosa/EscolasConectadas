    private  void inicializarTabela(){
        String [] colunas = {"Tipo", "Status","Observação"};
        String [][] dados =  new String[diretor.getPedidos().size()][colunas.length];
        String status;
        for (int i = 0; i < diretor.getPedidos().size(); i++) {
            Pedido pedido = diretor.getPedidos().get(i);
            if (pedido.isApto()) {
                status = "Pedido Aceito";
            }
            else{
                status = "Em Processamento";
            }
            String [] dado = {pedido.getTipo(), status, pedido.getObs()};
            dados[i] = dado;
        }
        modeloTabela = new DefaultTableModel(dados, colunas){
            @Override
            public boolean isCellEditable(int row, int column) {                
                return false; 
            };  
        };    
    }
