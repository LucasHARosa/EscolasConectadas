    private void botaoRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoRemoverActionPerformed
        int indice = jTable1.getSelectedRow();
        if (indice != -1) {
            try {
                diretor.removerPedido(indice);
                ((DefaultTableModel) jTable1.getModel()).removeRow(indice);
            } catch (IOException | ClassNotFoundException e) {
                JOptionPane.showMessageDialog(null,"Ocorreu um Erro Durante a Remoção do Pedido (" + e.toString() + ")" ,"ERRO" ,JOptionPane.ERROR_MESSAGE);
            }
        }
        else{
            JOptionPane.showMessageDialog(null,"Por favor escolha um pedido para ser deletado","AVISO" ,JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_botaoRemoverActionPerformed

